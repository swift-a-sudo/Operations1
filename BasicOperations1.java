public class BasicOperations1{
    
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int sum = a + b;
        int diff = a - b;
        int prod = a * b;
        System.out.println("Addition: " + sum);
        System.out.println("Subtraction: " + diff);
        System.out.println("Multiplication: " + prod);
    }
}
